package First_TWENTYSHAPES;

import java.awt.*;

public abstract class Shape {
    int x, y, width, height;
    Color color;

    public int getX() {
        return x;
    }

    public int getY() {
        return y;
    }

    public int getWidth() {
        return width;
    }

    public int getHeight() {
        return height;
    }

    public Color getColor() {
        return color;
    }
}
